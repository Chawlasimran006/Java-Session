package com.Simran715.Day39;

public class GreaterThan5 {
    public static void main(String []args)
    {
        int i=1;
        while(i<=10)
        {
            if(i<=5)
                System.out.println(i+ "- Low");
            else
                System.out.println(i+ "- High");
            i++;
        }

    }
}
